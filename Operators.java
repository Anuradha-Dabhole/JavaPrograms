package basicprogram;

public class Operators {

	public static void main(String[] args) 
	{
		int a =100;
		int b =200;
		int add =a+b;
		System.out.println("Addition of 2 numbers:" +add);
		int sub =b-a;
		System.out.println("Substraction of 2 numbers:"+sub);
		int multi=a*b;
		System.out.println("Multiplication of 2 numbers:"+multi);
		int div=a/b;
		System.out.println("Division of 2 numbers:"+div);
		int modulus=a/b;
		System.out.println("Modulus of 2 numbers:"+modulus);
	}

}
